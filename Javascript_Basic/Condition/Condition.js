//Câu lệnh switch trong JavaScript được sử dụng để kiểm tra giá trị của biến và thực hiện các hành động khác nhau tương ứng với từng giá trị
/*
switch (biến) {
  case giá trị 1:
     Thực hiện hành động 1
    break;
  case giá trị 2:
 Thực hiện hành động 2
    break;
  ...
  default:
   Thực hiện hành động mặc định
}
*/

var thu = 9;

switch (thu) {
  case 2:
    console.log("thứ 2");
    break;
  case 3:
    console.log("thứ 3");
    break;
  case 4:
    console.log("thứ 4");
    break;
  case 5:
    console.log("thứ 5");
    break;
  case 6:
    console.log("thứ 6");
    break;
  case 7:
    console.log("thứ 7");
    break;
  case 8:
    console.log("chủ nhật");
    break;
  default:
    console.log("Đéo có thứ nào hết")
    break;
}
